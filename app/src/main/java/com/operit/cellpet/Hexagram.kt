    /**
     * 从细胞状态计算卦象索引（0~63）
     * 初爻=atp, 二爻=damage, 三爻=cortisol, 四爻=children, 五爻=dopamine, 上爻=age
     */
    fun computeHexagram(
        atp: Float, damage: Float, cortisol: Float,
        children: Int, dopamine: Float, age: Long
    ): Int {
        val l1 = if (atp > 5f) 1 else 0          // 初爻：能量足→阳
        val l2 = if (damage < 0.3f) 1 else 0      // 二爻：损伤轻→阳
        val l3 = if (cortisol < 2f) 1 else 0      // 三爻：压力低→阳
        val l4 = if (children > 0) 1 else 0        // 四爻：有子代→阳
        val l5 = if (dopamine > 1f) 1 else 0       // 五爻：多巴胺足→阳
        val l6 = if (age < 100) 1 else 0           // 上爻：年轻→阳
        return (l6 shl 5) or (l5 shl 4) or (l4 shl 3) or (l3 shl 2) or (l2 shl 1) or l1
    }

    /** 获取上卦索引 (0~7) */
    fun upperTrigram(hexIndex: Int): Int = (hexIndex shr 3) and 0x7

    /** 获取下卦索引 (0~7) */
    fun lowerTrigram(hexIndex: Int): Int = hexIndex and 0x7

    /** 格式化变爻信息 */
    fun lineName(lineIndex: Int, isYang: Boolean): String {
        val pos = when (lineIndex) {
            0 -> "初"; 1 -> "二"; 2 -> "三"; 3 -> "四"; 4 -> "五"; 5 -> "上"
            else -> ""
        }
        return pos + if (isYang) "九" else "六"
    }
